  <div class="glossary_items">

  <div class="glossary_item" id="annual-mileage">
    <h3> Annual mileage </h3>
    <p> As part of your leasing contract you will agree to stick to an annual mileage limit. This is because the
mileage you do is closely linked to the depreciation of the vehicle, and the monthly fee you pay is for t
depreciation of the vehicle over time. The annual mileage is normally anything from 5,000 to 50,000
miles per year. </p>
  </div>

  <div class="glossary_item" id="balloon-payment">
    <h3> Balloon payment </h3>
    <p> A feature of Contract Purchases and Personal Contract Purchase, this is a final payment that is agreed a
the start of the contract but not made until the end. The payment is of higher value than your regular
payment charges and once paid, you take ownership of the vehicle. </p>
  </div>

  <div class="glossary_item" id="benefit-in-kind">
    <h3> BIK (benefit in kind) </h3>
    <p> Benefit in kind are benefits which employees or directors receive from their employment but which are
not included in their salary cheque or wage. If you use a company car for private use, you will have to
pay benefit in kind tax. How much depends on a number of factors, use our BIK calculator to estimate
how much you will pay. </p>
  </div>

  <div class="glossary_item" id="business-contract-hire">
    <h3> Business contract hire (BCH) </h3>
    <p> A business contract hire agreement allows you to lease a vehicle a set period of time which is typically
between 24 and 60 months. During that time you pay an agreed monthly fee and then vehicle is then
returned at the end of the term. </p>
  </div>

  <div class="glossary_item" id="business-contract-purchase">
    <h3> Business contract purchase (BCP) </h3>
    <p> With business contract purchase you pay a fixed monthly payment with the option to buy the vehicle at the end of the contract term. Alternatively, you can part exchange the vehicle for a new one using any equity for the deposit, or you can hand the vehicle back to AMT (subject to mileage and condition). </p>
  </div>

  <div class="glossary_item" id="business-lease-purchase">
    <h3> Business lease purchase (BLP) </h3>
    <p> This option is similar to hire purchase, the difference being that an agreement to purchase the vehicle at the end of the term is made in advance, with no option for the purchaser to change their mind at the conclusion of the term.  </p>
  </div>


  <div class="glossary_item" id="bvrla">
    <h3> BVRLA </h3>
    <p> The BVRLA is the trade body for the vehicle rental and leasing sector. It seeks to look after the interests of, and sets standards for operational quality for, both the contract hire and leasing sectors and the daily hire (<a href="https://en.wikipedia.org/wiki/Car_rental"> car rental </a>) sectors.   </p>
  </div>

  <div class="glossary_item" id="contract-hire">
    <h3> Contract Hire </h3>
    <p> Contract hire is another word for leasing and is available on a business or personal basis. </p>
  </div>

  <div class="glossary_item" id="contract-length">
    <h3> Contract length </h3>
    <p> When you lease a vehicle, you can choose a contract length which is normally from 2 to 5 years. For the term of the contract you agree to pay a fixed amount each month. </p>
  </div>

  <div class="glossary_item" id="depreciation">
    <h3> Depreciation </h3>
    <p> The reduction of value of an asset over time, in this case a vehicle. The reduction in value is particularly due to wear and tear. </p>
  </div>

  <div class="glossary_item" id="document-fee">
    <h3> Document fee </h3>
    <p> A fee that may be charged to process a vehicleâ€™s paperwork. </p>
  </div>

  <div class="glossary_item" id="excess-mileage-charge">
    <h3> Excess mileage charge </h3>
    <p> Mileage covered that is additional to that agreed in the contract. It always results in a charge to cover the additional servicing and depreciation costs it generates. </p>
  </div>

  <div class="glossary_item" id="fleet-management">
    <h3> Fleet management </h3>
    <p> Fleet management refers to a service which helps to minimize the risks associated with vehicle investment, improve efficiency, productivity and reduce overall transportation costs. If you need six or more vehicles for your business, we can offer you a fleet management solution. Just get in touch today. </p>
  </div>

  <div class="glossary_item" id="funder">
    <h3> Funder </h3>
    <p> A funder is an organisation that provides finance for vehicles. We have partnerships with 5 finance companies so we can remain impartial and have a choice between funders when it comes to securing finance on your lease.  </p>
  </div>

  <div class="glossary_item" id="gap-insurance">
    <h3> GAP insurance </h3>
    <p> Guaranteed Asset Protection or GAP insurance is a financial product often sold if you buy or lease a brand-new car. If your car is stolen or written off (total loss), GAP insurance covers the difference between the current value of the car (the amount your car insurer will usually pay out) and the amount of outstanding payments you still owe.  </p>
  </div>

  <div class="glossary_item" id="hire-purchase">
    <h3> Hire purchase </h3>
    <p> Hire purchase is a finance option for business and personal leasing customers. A deposit payment is agreed - typically a multiple of the monthly payment - and repayments will be calculated based on this figure, the value of the vehicle, and the length of the repayment term. Once all the repayments have been completed, the business will become the legal owner of the vehicle.  </p>
  </div>

  <div class="glossary_item" id="initial-payment">
    <h3> Initial payment </h3>
    <p> This your first payment for your leased vehicle. It is usually calculated as 3, 6 or 9 months of your monthly rental. The higher the initial payment, the lower your subsequent monthly rental payments will be.  </p>
  </div>


  <div class="glossary_item" id="lease-purchase">
    <h3> Lease purchase (LP) </h3>
    <p> Lease purchase is a similar finance agreement to hire purchase, the difference being that at the end of the contract, you can either make a balloon payment and take ownership of the vehicle or, or you can choose to part exchange your vehicle â€“ you make the balloon payment then lease a newer car.  </p>
  </div>

  <div class="glossary_item" id="maintenance">
    <h3> Maintenance </h3>
    <p> Maintenance is an optional additional monthly cost that covers you for servicing, replacement tyres and all costs associated with mechanical and electrical repairs as well as bulbs, batteries, exhausts and so on. Maintenance packages are available to both business and personal leasing customers.  </p>
  </div>

  <div class="glossary_item" id="manufacturer-warranty">
    <h3> Manufacturer warranty </h3>
    <p> All of our lease vehicles come with a manufacturer warranty which can range from 1 to 5 years. The warranty covers anything that is considered to be a manufacturing defect.   </p>
  </div>

  <div class="glossary_item" id="personal-contract-hire">
    <h3> Personal contract hire (PCH) </h3>
    <p> Personal contract hire is also known as personal leasing. It works in the same way as business leasing but the prices are generally slightly more expensive as they include VAT. You pay a monthly rental to lease a car over a set period of time, typically between 24 - 60 months, with a flexible initial payment structure of either 1, 3, 6, 9 or 12 months which is paid after delivery. The vehicle is then returned at the end of the term.  </p>
  </div>

  <div class="glossary_item" id="personal-contract-purchase">
    <h3> Personal contract purchase (PCP) </h3>
    <p> Similar to personal contract hire as you pay a monthly rental to lease a car, only you also have the option to purchase the vehicle at the end of the contract by paying a â€˜balloon paymentâ€™. The amount of the balloon payment is agreed at the beginning of your contract.  </p>
  </div>

  <div class="glossary_item" id="processing-fee">
    <h3> Processing fee </h3>
    <p> The processing fee is an administration charge/set up fee is implemented to cover the cost of processing the finance application & dealing with the contract documentation.  </p>
  </div>

  <div class="glossary_item" id="residual-value">
    <h3> Residual value </h3>
    <p> An estimate of what the vehicle will be worth at the end of the contract.  </p>
  </div>


  <div class="glossary_item" id="wear-and-tear">
    <h3> Wear and tear </h3>
    <p> A BVLA guide that clarifies the definitions of fair wear and tear at the end of a lease. AMT Leasing adheres to this guide.  </p>
  </div>


