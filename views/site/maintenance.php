  <h3> What is a maintenance package? </h3>
    <p> Maintenance is an optional additional monthly cost that covers you for servicing, replacement tyres and all costs associated with mechanical and electrical repairs as well as bulbs, batteries, exhausts and so on. Maintenance packages are available to both business and personal leasing customers. </p>

      <h3> The benefits </h3>
        <p><span> Peace of mind </span></p>
        <p> Should the vehicle need any unforeseen repairs, you know you're covered. </p>

        <p><span> Spread the cost </span></p>
        <p> As you pay for maintenance on a monthly basis, the cost is spread so you can avoid any large bills. </p>

        <p><span> Uninterrupted motoring </span></p>
        <p> If you've included the options to have a replacement vehicle whilst your vehicle is being repaired, there won't be any inconvenience. </p>

        <p><span> Quality service </span></p>
        <p> Your vehicle will be repaired at an approved garage by qualified technicians. </p>

  <h3> What's covered in a maintenance agreement? </h3>
      <ul class="positive_list">
          <li> Mechanical and electrical repairs or replacements, including associated parts and labour due to fair wear and tear </li>
          <li> Bulbs, Batteries, Exhausts, Cam Belts, Wiper Blades, Alternators &amp; Starter Motors. </li>
          <li> MOT tests and all servicing. </li>
          <li> Breakdown cover. </li>
          <li> Unlimited premium branded tyre replacements including valve and balance. </li>
          <li> Free mobile tyre fitting service at a convenient location for you by appointment. </li>
          <li> No recharges for punctures or damaged tyres (dependent upon finance provider).</li>
      </ul>

    <h3> Any exclusions? </h3>
    <ul class="negative_list">
      <li> Windscreen repairs and / or glass replacement </li>
      <li> Routine top ups of fluids e.g. engine oil or washer fluid </li>
      <li> Repairs or replacements due to driver error or driver induced faults and accident damage. </li>
      <li> Missing or broken items e.g. bent aerials, missing hub caps. </li>
      <li> Repairs as a result of accidental damage, vandalism, misuse, theft, frost damage or fuel contamination </li>
    </ul>

<h3> Booking in for a service, MOT or repairs </h3>
<p> When you take out your leasing and maintenance contract, you will receive a maintenance contact number from the finance company. Just call this number and you can book the vehicle into a local approved garage. Most maintenance packages also come with free vehicle collection and delivery and if you've included a courtesy car option, this can be delivered to you when your vehicle is taken away for repair - simple! </p>

<h3> The costs </h3>
<p> Maintenance package costs vary and they're dependent on the vehicle you choose, type of contract you take and your expected annual mileage. The more miles you do, the more the wear and tear which will result in a higher maintenance cost. </p>
<p> Once you've found the vehicle you're interested in, give us a call and we can provide you with a quote including and exclusing maintenance so you can easily compare. Alternatively, you can add this option online to view the price change. </p>

<h3> Is it right for me? </h3>
<p> We will never recommend a product to you which isn't right for your needs, and the choice is always yours. Our Account Managers can give you quotes with and without maintenance included so you can make a balanced decision.</p>


